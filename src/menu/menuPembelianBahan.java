    public void getDataDetail(DefaultTableModel model, String id) {
        model.setRowCount(0);
        
        try {
           String sql = "SELECT * FROM detail_pembelian_bahan WHERE id_pembelian_bahan = '"+id+"'";
           try (PreparedStatement st = conn.prepareStatement(sql)) {
               ResultSet rs = st.executeQuery();
               
               while (rs.next()) {
                    String idPembelianBahan = rs.getString("id_pembelian_bahan");
                    String idBahan          = rs.getString("id_bahan");
                    int jumlah               = rs.getInt("jumlah");
                    String satuan            = rs.getString("satuan");
                    int hargaBeli           = rs.getInt("harga_beli");
                    

                    Object[] rowData = {idPembelianBahan, idBahan, jumlah, satuan,hargaBeli };
                    model.addRow(rowData);
                }
           }
        }catch (Exception e) {
            Logger.getLogger(menuPembelianBahan.class.getName()).log(Level.SEVERE,null, e);
        }
    }

    public void getData(int startIndex, int entriesPage, DefaultTableModel model) {
        model.setRowCount(0);
        
        try {
           String sql = "SELECT pb.id_pembelian_bahan, pb.tgl_pembelian, pb.total_harga_pembelian, usr.nama FROM pembelian_bahan pb "
                   + "INNER JOIN user usr ON pb.id_user = usr.id_user ORDER BY pb.id_pembelian_bahan ASC LIMIT ?,?";
           try (PreparedStatement st = conn.prepareStatement(sql)) {
               st.setInt(1, startIndex);
               st.setInt(2, entriesPage);
               ResultSet rs = st.executeQuery();
               
               while (rs.next()) {
                    String idPembelianBahan = rs.getString("id_pembelian_bahan");
                    String tglPembelian = rs.getString("tgl_pembelian");
                    int totalHargaPembelian = rs.getInt("total_harga_pembelian");
                    String namaUser = rs.getString("nama");

                    Object[] rowData = {idPembelianBahan, tglPembelian, totalHargaPembelian, namaUser};
                    model.addRow(rowData);
                }
           }
        }catch (Exception e) {
            Logger.getLogger(menuPembelianBahan.class.getName()).log(Level.SEVERE,null, e);
        }
    }

    private String setIdPembelianBahan() {
    String urutan = null;
    String prefix = "TRB";

    try {
        // Ambil semua ID yang ada
        String sql = "SELECT CAST(SUBSTRING(id_pembelian_bahan, 4) AS UNSIGNED) AS Nomor FROM pembelian_bahan WHERE id_pembelian_bahan LIKE ? ORDER BY Nomor ASC";
        try (PreparedStatement st = conn.prepareStatement(sql)) {
            st.setString(1, prefix + "%");
            ResultSet rs = st.executeQuery();

            int expectedNomor = 1; // Mulai dari 1
            while (rs.next()) {
                int currentNomor = rs.getInt("Nomor");
                if (currentNomor != expectedNomor) {
                    // Jika ada gap, gunakan expectedNomor sebagai ID baru
                    urutan = prefix + String.format("%04d", expectedNomor);
                    return urutan;
                }
                expectedNomor++;
            }
            urutan = prefix + String.format("%04d", expectedNomor);
        }
    } catch (Exception e) {
        Logger.getLogger(menuPembelianBahan.class.getName()).log(Level.SEVERE, null, e);
    }

    return urutan;
    }
    
