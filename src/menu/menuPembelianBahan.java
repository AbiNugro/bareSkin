    private int calculateStok(int jumlahBeli, String satuan) {
    int konversi = 1;
    
    switch (satuan) {
        case "Renteng":
            konversi = 10; 
            break;
        case "KiloGram":
            konversi = 1000; 
            break;
        case "Slop Kecil":
            konversi = 50; 
            break;
        case "Slop Besar":
            konversi = 25; 
            break;
        case "Pak":
            konversi = 500; 
            break;
        case "Roll":
            konversi = 1000; 
            break;
        case "Kotak Susu":
            konversi = 2500; 
            break;
        case "Gram":
        case "Item":
            konversi = 1;
            break;
    }
    return jumlahBeli * konversi;
}
    
